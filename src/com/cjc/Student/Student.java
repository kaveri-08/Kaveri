package com.cjc.Student;

import com.cjc.Batch.Batch;

public class Student {

	int sid;
	String name;
	Batch sbatch;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Batch getSbatch() {
		return sbatch;
	}
	public void setSbatch(Batch sbatch) {
		this.sbatch = sbatch;
	}
	
	
}
